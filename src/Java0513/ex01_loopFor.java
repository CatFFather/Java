package Java0513;

public class ex01_loopFor {

	public static void main(String[] args) {
	
		 // 반복문 For
		/*
		  	for(초기화식; 조건식; 증강식){
		  		반복할수행문
		  	}
		 */
		int i;
		for(i=1; i<=10; i++) {
			System.out.println(i);
		}
		System.out.println("반복문 종료 후 i 값 : " + i);
		
		
		
	}

}
