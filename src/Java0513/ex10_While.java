package Java0513;

public class ex10_While {

	public static void main(String[] args) {

		// while

		/*
		  while(반복조건){
		  
		  실행내용
		  
		  }
		  
		 */

		int i = 1;
		int sum = 0;

		while (i <= 10) {
			sum = sum + i;
			i++;

		}
		System.out.print(sum);

		// for(초기화식 ; 조건식; 증감식)

		// 초기화식
		// while(조건식){
		// 증감식
		// }
								//필드에서는 for를 더 많이 씀

	}

}
